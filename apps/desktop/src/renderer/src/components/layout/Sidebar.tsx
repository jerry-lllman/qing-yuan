interface SidebarProps {
  className?: string;
}

export function Sidebar({ className }: SidebarProps) {
  const navigate = useNavigate();
  const user = useAuthStore((state) => state.user);
  const logout = useAuthStore((state) => state.logout);

  const handleLogout = () => {
    logout();
    navigate('/login', { replace: true });
  };

  return (
    <aside
      className={`w-16 bg-muted/50 border-r flex flex-col items-center py-4 ${className || ''}`}
    >
      {/* 用户头像 */}
      <Avatar className="w-10 h-10 mb-6">
        <AvatarImage src={user?.avatar || undefined} />
        <AvatarFallback>{user?.nickname?.[0] || 'U'}</AvatarFallback>
      </Avatar>

      {/* 导航图标 */}
      <nav className="flex-1 flex flex-col items-center gap-2">
        <Button variant="ghost" size="icon" className="w-10 h-10" title="消息">
          <ChatIcon />
        </Button>
        <Button variant="ghost" size="icon" className="w-10 h-10" title="联系人">
          <ContactIcon />
        </Button>
      </nav>

      {/* 底部操作 */}
      <div className="flex flex-col items-center gap-2">
        <Button variant="ghost" size="icon" className="w-10 h-10" title="设置">
          <SettingsIcon />
        </Button>
        <Button
          variant="ghost"
          size="icon"
          className="w-10 h-10 text-destructive hover:text-destructive"
          title="退出登录"
          onClick={handleLogout}
        >
          <LogoutIcon />
        </Button>
      </div>
    </aside>
  );
}
